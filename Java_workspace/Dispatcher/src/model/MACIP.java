package model;

public class MACIP {
	protected int ID;
	protected String MAC;
	protected String IP;
	protected int VMID;
	/**
	 * @return the iD
	 */
	public int getID() {
		return ID;
	}
	/**
	 * @param iD the iD to set
	 */
	public void setID(int iD) {
		ID = iD;
	}
	/**
	 * @return the mAC
	 */
	public String getMAC() {
		return MAC;
	}
	/**
	 * @param mAC the mAC to set
	 */
	public void setMAC(String mAC) {
		MAC = mAC;
	}
	/**
	 * @return the iP
	 */
	public String getIP() {
		return IP;
	}
	/**
	 * @param iP the iP to set
	 */
	public void setIP(String iP) {
		IP = iP;
	}
	/**
	 * @return the vMID
	 */
	public int getVMID() {
		return VMID;
	}
	/**
	 * @param vMID the vMID to set
	 */
	public void setVMID(int vMID) {
		VMID = vMID;
	}
	
	
}
